public class ForPractice1 {

	/**
	 * This is the entry point to the program.
	 * @param args
	 */
	public static void main(String args[]) {

		// For-loop that will print "Hello" ten times.
		for (int i = 0 ; i < 10 ; i++) {
			System.out.println("Hello");
		}

	}

}
